/**
 * Obtiene la frase del día basada en la fecha
 */
export const getFraseDelDia = () => {
  const today = new Date();
  const dayOfYear = Math.floor((today - new Date(today.getFullYear(), 0, 0)) / (1000 * 60 * 60 * 24));
  const index = dayOfYear % FRASES_DIARIAS.length;
  return FRASES_DIARIAS[index];
};

/**
 * Obtiene un saludo aleatorio
 */
export const getSaludoAleatorio = (nombre, autor, libro) => {
  const index = Math.floor(Math.random() * SALUDOS_INTRO.length);
  return SALUDOS_INTRO[index](nombre, autor, libro);
};

